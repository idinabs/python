# operasi assignment / penugasan

a = 8
# penjumlahan
print("\n=======+")
a += 2
print("hasil a adalah :",a,"\n")

# pengurangan
print("\n=======-")
a -= 2
print("hasil a adalah :",a)

# pembagian

print("\n=======/")
a /= 2
print("hasil b adalah :",a)

b = 10
# perkalian
print("\n=======*")
b *= 4
print("\nhasil b adalah :",b)

print("\n=======%")
b %= 2
print("\nhasil b adalah :",b)

# eksponen / pangkat
print("\n=======**")
b **= 4
print("\nhasil b adalah :",b)


# bitwise
print("\n=======OR")
a = True
a |= False
print("hasilnya adalah :",a)

print("\n=======AND")
a &= False
print("hasilnya adalah :",a)

print("\n=======XOR")
a ^= False
print("hasilnya adalah :",a)


# biner yang dicari
c = 9

print("hasil c adalah :",format(c,"08b"))









