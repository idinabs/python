# Operasi aritmatika

print("=====Penjumlahan=====")

a = 5
b = 2

hasil = a + b
print("ini adalah hasil a + b =",hasil)


print("=====PENGURANGAN=====")
pengurangan = a - b
print("ini adalah hasil a - b =",pengurangan)


print("=====PERKALIAN====")
perkalian = a * b
print("ini adalah hasil a * b =",perkalian)

print("=====PEMBAGIAN====")
pembagian = a / b
print("ini adalah hasil a / b =",pembagian)

print("=====EKSPONEN=====")
perpangkatan = a ** b
print("ini adalah hasil a ** b =",perpangkatan)

print("======MODULUS=====")
hasil_bagi = a % b
print("ini adalah hasil a", "%", "b =",hasil_bagi)

print("======FLOOR DIVISION=====")
hasil1 = a // b
print("ini adalah hasil a", "//", "b =",hasil1)




